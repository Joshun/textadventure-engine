#define COLOUR_RED   "\033[0;31m"        /* 0 -> normal ;  31 -> red */
#define COLOUR_CYAN  "\033[1;36m"        /* 1 -> bold ;  36 -> cyan */
#define COLOUR_GREEN "\033[4;32m"        /* 4 -> underline ;  32 -> green */
#define COLOUR_BLUE  "\033[9;34m"        /* 9 -> strike ;  34 -> blue */
 
#define COLOUR_BLACK  "\033[0;30m"
#define COLOUR_BROWN  "\033[0;33m"
#define COLOUR_MAGENTA  "\033[0;35m"
#define COLOUR_GREY  "\033[0;37m"
 
#define COLOUR_NONE   "\033[0m"        /* to flush the previous property */
